#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = process.cwd();

function parseArg(name) {
  const exact = `--${name}`;
  const pref = `--${name}=`;
  const idx = process.argv.indexOf(exact);
  if (idx >= 0 && process.argv[idx + 1]) return process.argv[idx + 1];
  const hit = process.argv.find((arg) => arg.startsWith(pref));
  return hit ? hit.slice(pref.length) : '';
}

function normalizeBaseUrl(input) {
  const raw = String(input || '').trim();
  if (!raw) return '';
  if (!/^https?:\/\//i.test(raw)) return `https://${raw.replace(/^\/+/, '')}`.replace(/\/+$/, '');
  return raw.replace(/\/+$/, '');
}

function safeUrl(base, suffix) {
  return `${base}${suffix.startsWith('/') ? suffix : `/${suffix}`}`;
}

async function requestCheck(baseUrl, check) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), check.timeoutMs || 12000);
  const started = Date.now();

  try {
    const response = await fetch(safeUrl(baseUrl, check.path), {
      method: check.method || 'GET',
      headers: {
        'accept': check.accept || '*/*',
        'x-closeflow-smoke': 'stage90d-no-secret',
        ...(check.headers || {}),
      },
      body: check.body,
      signal: controller.signal,
      redirect: 'manual',
    });

    const status = response.status;
    const ok = check.acceptStatuses.includes(status);
    return {
      id: check.id,
      method: check.method || 'GET',
      path: check.path,
      status,
      ok,
      expected: check.acceptStatuses,
      durationMs: Date.now() - started,
      note: check.note || '',
    };
  } catch (error) {
    return {
      id: check.id,
      method: check.method || 'GET',
      path: check.path,
      status: 'FETCH_ERROR',
      ok: false,
      expected: check.acceptStatuses,
      durationMs: Date.now() - started,
      note: `${check.note || ''} ${error?.name || 'Error'}: ${error?.message || String(error)}`.trim(),
    };
  } finally {
    clearTimeout(timeout);
  }
}

function resultLine(result) {
  return `| ${result.id} | ${result.method} ${result.path} | ${result.ok ? 'PASS' : 'FAIL'} | ${result.status} | ${result.expected.join(', ')} | ${result.durationMs} | ${String(result.note || '').replace(/\|/g, '/')} |`;
}

function writeReport(baseUrl, results, skippedReason) {
  const out = path.join(root, 'docs/qa/STAGE90D_LIVE_SMOKE_RESULT.latest.md');
  fs.mkdirSync(path.dirname(out), { recursive: true });

  const lines = [];
  lines.push('# Stage90D Live Smoke Result');
  lines.push('');
  lines.push(`GeneratedAt: ${new Date().toISOString()}`);
  lines.push('Package: CUMULATIVE_STAGE90D');
  lines.push(`BaseUrl: ${baseUrl || 'MISSING'}`);
  lines.push(`Status: ${skippedReason ? 'SKIPPED_NO_URL' : (results.every((r) => r.ok) ? 'PASS_AUTOMATED_SAFE_SMOKE' : 'FAIL_AUTOMATED_SAFE_SMOKE')}`);
  lines.push('');
  lines.push('## Safety');
  lines.push('');
  lines.push('- DIGEST_ROUTE_NOT_CALLED_BY_DEFAULT=True');
  lines.push('- NO_SECRET_VALUES_WRITTEN=True');
  lines.push('- NO_REAL_UPLOAD_PERFORMED=True');
  lines.push('- NO_AUTH_TOKEN_USED=True');
  lines.push('');
  if (skippedReason) {
    lines.push('## Skipped');
    lines.push('');
    lines.push(skippedReason);
    lines.push('');
  } else {
    lines.push('## Results');
    lines.push('');
    lines.push('| Check | Request | Result | Status | Expected | DurationMs | Note |');
    lines.push('|---|---|---:|---:|---|---:|---|');
    for (const result of results) lines.push(resultLine(result));
    lines.push('');
  }
  lines.push('## Manual checks still required');
  lines.push('');
  lines.push('- [ ] Browser notification permission + real notification.');
  lines.push('- [ ] Toast generated by NotificationRuntime in real browser.');
  lines.push('- [ ] Vercel cron logs for daily digest.');
  lines.push('- [ ] Resend delivery or safe dry-run proof.');
  lines.push('- [ ] Portal with valid token opens scoped session.');
  lines.push('- [ ] Real portal upload succeeds.');
  lines.push('- [ ] Invalid portal upload is blocked.');
  lines.push('- [ ] Button Matrix checklist updated after live click-through.');
  lines.push('');
  lines.push('## Decision');
  lines.push('');
  lines.push('This report is not a full production acceptance. It is a safe automated smoke layer.');

  fs.writeFileSync(out, lines.join('\n') + '\n', 'utf8');
  return out;
}

async function main() {
  const inputUrl = parseArg('url')
    || process.env.RELEASE_PREVIEW_URL
    || process.env.APP_URL
    || process.env.VERCEL_URL
    || '';
  const baseUrl = normalizeBaseUrl(inputUrl);

  if (!baseUrl) {
    const out = writeReport('', [], 'No RELEASE_PREVIEW_URL / APP_URL / VERCEL_URL / --url was provided.');
    console.log('LIVE_SMOKE_SKIPPED_NO_URL=True');
    console.log(`WROTE ${path.relative(root, out)}`);
    return;
  }

  const checks = [
    {
      id: 'app-root',
      method: 'GET',
      path: '/',
      acceptStatuses: [200, 301, 302, 307, 308],
      note: 'App shell should answer.',
    },
    {
      id: 'manifest-webmanifest',
      method: 'GET',
      path: '/manifest.webmanifest',
      acceptStatuses: [200, 404],
      note: '200 preferred. 404 acceptable if manifest path differs and static PWA guard covers it.',
    },
    {
      id: 'manifest-json',
      method: 'GET',
      path: '/manifest.json',
      acceptStatuses: [200, 404],
      note: 'Alternative manifest path.',
    },
    {
      id: 'service-worker-sw',
      method: 'GET',
      path: '/sw.js',
      acceptStatuses: [200, 404],
      note: '200 preferred. 404 acceptable if service worker path differs and static guard covers it.',
    },
    {
      id: 'service-worker-alt',
      method: 'GET',
      path: '/service-worker.js',
      acceptStatuses: [200, 404],
      note: 'Alternative service worker path.',
    },
    {
      id: 'portal-session-no-token',
      method: 'GET',
      path: '/api/portal?route=session',
      acceptStatuses: [400, 401, 403, 404, 405],
      note: 'No-token portal access must not return 200.',
    },
    {
      id: 'portal-upload-no-token',
      method: 'POST',
      path: '/api/storage-upload',
      acceptStatuses: [400, 401, 403, 404, 405],
      headers: { 'content-type': 'application/json' },
      body: JSON.stringify({ stage90d: true }),
      note: 'No-token upload must not return 200. No real file uploaded.',
    },
    {
      id: 'billing-checkout-method-guard',
      method: 'GET',
      path: '/api/billing-checkout',
      acceptStatuses: [400, 401, 403, 404, 405],
      note: 'GET should not create checkout or paid state.',
    },
  ];

  const results = [];
  for (const check of checks) {
    results.push(await requestCheck(baseUrl, check));
  }

  const out = writeReport(baseUrl, results, '');
  console.log(`LIVE_SMOKE_BASE_URL=${baseUrl}`);
  for (const result of results) {
    console.log(`${result.ok ? 'PASS' : 'FAIL'} ${result.id} ${result.status}`);
  }
  console.log('DIGEST_ROUTE_NOT_CALLED_BY_DEFAULT=True');
  console.log(`WROTE ${path.relative(root, out)}`);

  if (!results.every((result) => result.ok)) {
    process.exit(1);
  }
}

main().catch((error) => {
  console.error(error?.stack || error?.message || String(error));
  process.exit(1);
});
